package com.template;

import java.util.*;

public class Solution {
  public static void main(String[] args) {
    try (Scanner in = new Scanner(System.in)) {
      int T = in.nextInt();

      for (int t = 1; t <= T; ++t) {
        System.out.println(
                "Case #" + t + ": " + ".");
      }
    }
  }
}
